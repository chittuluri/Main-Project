    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String ciphertext=jTextArea1.getText().trim();
        String key=jTextField3.getText().trim();
        
        String[] s = key.split(",");        
        String d = s[0];
        String n = s[1];            

                //Decrypting, M' = C^d mod n

                    /*Decrypting cipher*/
                
        String[] cip = ciphertext.split(",");
        String file = "";
        for (int i = 0; i < cip.length; i++)
        {                        
            String dec = new BigInteger(cip[i]).modPow(new BigInteger(d), new BigInteger(n)).toString();                                            
            int ascii = Integer.parseInt(dec.trim());
            char ori=(char)ascii;                    
            file = file + ori;
        }
        JOptionPane.showMessageDialog(new JFrame(),"Decrypted Successfully!");
        jTextArea1.setText(file.trim());
    }//GEN-LAST:event_jButton4ActionPerformed

