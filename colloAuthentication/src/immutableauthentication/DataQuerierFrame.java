    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:        
        String topics=jTextField1.getText().trim();
        
        try
        {
            int count=0;
            ResultSet rs=st.executeQuery("select * from Upload");
            while(rs.next())
            {
                String ownername=rs.getString(1);
                String filename=rs.getString(2);
                String tags=rs.getString(3);
                String ciphertext=rs.getString(4);
                String cloudservers=rs.getString(5);
                if(tags.contains(","))
                {
                    String tg[]=tags.split(",");
                    for(int i=0;i<tg.length;i++)
                    {
                        if(tg[i].toLowerCase().trim().equals(topics.toLowerCase().trim()))
                        {
                            jTextField2.setText(filename.trim());
                            jTextArea1.setText(ciphertext.trim());
                            count=1;
                        }
                    }
                }
                else
                {
                    if(tags.toLowerCase().trim().equals(topics.toLowerCase().trim()))
                    {
                        jTextField2.setText(filename.trim());
                        jTextArea1.setText(ciphertext.trim());
                        count=1;
                    }
                }
            }
            if(count!=1)
            {
                JOptionPane.showMessageDialog(new JFrame(),"Nothing Found!");
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

